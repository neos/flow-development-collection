/**
 * Validation utilities for the Kickstarter.
 *
 */
class Validation
{
    /**
     * Check the given keyword to be not one of the reserved words of PHP.
     *
     * @param string $keyword
     * @return boolean
     * @see http://www.php.net/manual/en/reserved.keywords.php
     */
    public static function isReservedKeyword($keyword)
    {
        $reservedKeywords = array(
            '__halt_compiler',
            'abstract',
            'and',
            'array',
            'as',
            'break',
            'callable',
            'case',
            'catch',
            'class',
            'clone',
            'const',
            'continue',
            'declare',
            'default',
            'die',
            'do',
            'echo',
            'else',
            'elseif',
            'empty',
            'enddeclare',
            'endfor',
            'endforeach',
            'endif',
            'endswitch',
            'endwhile',
            'eval',
            'exit',
            'extends',
            'final',
            'for',
            'foreach',
            'function',
            'global',
            'goto',
            'if',
            'implements',
            'include',
            'include_once',
            'instanceof',
            'insteadof',
            'interface',
            'isset',
            'list',
            'namespace',
            'new',
            'or',
            'print',
            'private',
            'protected',
            'public',
            'require',
            'require_once',
            'return',
            'static',
            'switch',
            'throw',
            'trait',
            'try',
            'unset',
            'use',
            'var',
            'while',
            'xor'
        );

        return in_array(strtolower($keyword), $reservedKeywords);
    }
}
